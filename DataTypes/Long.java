package DataTypes;

public class Long {
    public static void main(String[] args) {
        //Long can store -9223372036854775808L - 9223372036854775807L
        long num = 982958723957239L;
        long numb = -9223372036854775808L;
        System.out.println(num);
        System.out.println(numb);
        System.out.println(num + numb);

    }
}
